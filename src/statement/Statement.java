package statement;

import java.util.Date;

public class Statement {
    static void main(String[] args) {
        // Statement -> kalimat lengkap dalam bahasa
        // Sebuah statement berisikan execution komplit, biasa di akhiri dengan titik koma
        // Beberapa jenis statement -> Assigment expression, Penggunaan ++ dan --, Method invocation, Object creation expression

        // assigment statement
        double aValue = 8933.234;

        // increment statement
        aValue++;

        // method invocation statement
        System.out.println("Hello World");

        // object creation statement
        Date date = new Date();

    } // block kurung kurawa
}
